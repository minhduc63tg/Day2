package java_day2;

public class TacGia {
	private String name;
	private Ngay ngaysinh;
	public TacGia(String name, Ngay ngaysinh) {
		this.name = name;
		this.ngaysinh = ngaysinh;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Ngay getNgaysinh() {
		return ngaysinh;
	}
	public void setNgaysinh(Ngay ngaysinh) {
		this.ngaysinh = ngaysinh;
	}
	
	
	
}
